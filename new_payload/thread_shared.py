# ---------------------------------------------------------------------------
# Payload state management
# ---------------------------------------------------------------------------

class PayloadState:
    """Payload state enumeration."""

    IDLE = 0
    CAPTURING = 1
    RUNNING = 2
    TURNING_ON = 3
    DOWNSCALING = 4
    PREFILTERING = 5
    INFERENCE = 9
    DOWNLOAD = 6
    OFF = 7
    FAIL = 8


class StateManager:
    """Thread-safe manager for payload state."""

    def __init__(self, initial_state: int = PayloadState.IDLE):
        self._lock = threading.Lock()
        self._state = initial_state

    def get(self) -> int:
        """Get current state."""
        with self._lock:
            return self._state

    def set(self, state: int) -> None:
        """Set current state."""
        with self._lock:
            self._state = state


# Global state manager instance
state_manager = StateManager(PayloadState.IDLE)
